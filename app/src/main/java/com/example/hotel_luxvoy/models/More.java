package com.example.hotel_luxvoy.models;

public class More {
    private int image;
    private String description;

    public More() {
    }

    public More(int image, String description) {
        this.image = image;
        this.description = description;
    }

    public int getImage() {
        return image;
    }

    public String getDescription() {
        return description;
    }
}
